			<h2 class="mt-16 text-3xl font-bold tracking-tight text-gray-900">
				What do chinchillas eat?
			</h2>
			<p class="mt-6">
				Chinchillas are herbivores, which means they only eat plants. Their diet mainly consists of
				hay, pellets, and specific treats. They also need to have access to fresh water at all
				times. Chinchillas are very sensitive to sugar and fat, so they should not be fed any
				fruits, vegetables, or other treats that contain sugar or fat. They also should not be fed
				any dairy products, as they are lactose intolerant. They have very sensitive digestive
				systems, they require a diet that is high in fiber and low in fat.
			</p>

